function clickInfo1() {
    const alwaysSpan = document.querySelectorAll("#alwaysSpan");
    const startI = document.querySelectorAll("#startI");
    const noneSpan = document.querySelectorAll("#noneSpan");
    const flipI = document.querySelectorAll("#flipI");
    const click = document.querySelectorAll("#button");
  
    click[0].addEventListener("focusin", () => {
      startI[0].style.display = "none";
      flipI[0].style.display = "block";
      noneSpan[0].style.display = "block";
    });
    click[0].addEventListener("focusout", () => {
      startI[0].style.display = "block";
      flipI[0].style.display = "none";
      noneSpan[0].style.display = "none";
    });
  }
